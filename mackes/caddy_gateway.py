def install_ca_into_trust_store() -> list[str]:
    """Copy Caddy's internal CA root into the system trust store."""
    actions: list[str] = []
    # Caddy stores its internal CA at /var/lib/caddy/.local/share/caddy/pki/
    candidates = [
        Path("/var/lib/caddy/.local/share/caddy/pki/authorities/local/root.crt"),
        Path("/etc/caddy/pki/authorities/local/root.crt"),
        MESH_CA_CERT,
    ]
    src = next((c for c in candidates if c.exists()), None)
    if src is None:
        return ["Caddy CA root not found yet — start the gateway first"]
    dest = Path("/etc/pki/ca-trust/source/anchors/mackes-mesh-ca.crt")
    rc, _, err = _pkexec(["install", "-m", "0644", str(src), str(dest)], timeout=5)
    if rc != 0:
        return [f"could not install CA cert: {err.strip()}"]
    actions.append(f"installed {src.name} -> {dest}")
    rc2, _, err2 = _pkexec(["update-ca-trust", "extract"], timeout=15)
    actions.append(f"update-ca-trust rc={rc2} {err2.strip()}")
    for line in actions:
        log_action(line)
    return actions


